# 1 тб = 1 048 576 мб

dct = {
    'кб': 1,
    'мб': 2,
    'гб': 3,
    'тер': 4,
    'пет': 5
}


def convert(s1, s2, num):
    step = dct[s1] - dct[s2]
    result = num * 1024**step
    print(result)

convert('гб', 'кб', 1)

